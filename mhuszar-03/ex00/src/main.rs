fn choose<T>(values: &[T]) -> &T
{
    &values[ftkit::random_number(0..10000) as usize % values.len()]
}

fn main()
{
    let numbers: [i32; 10] = [1, 2, 3, 4, 5, 6, 7, 8, 9, 19];
    std::println!("{}", choose::<i32>(&numbers));
}